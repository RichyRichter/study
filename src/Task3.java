public class Task3 {
    public static void showLogical(){
        boolean a = true;
        boolean b = true;

        System.out.println("Операции с boolean:\n" +
                "a = " + a + " b = " + b + "\n"
                + "!a: " + !a + "\n"
                + "a & b: " + (a & b) + "\n"
                + "a && b: " + (a && b) + "\n"
                + "a | b: " + (a|b) + "\n"
                + "a || b: " + (a||b) + "\n"
                + "a ^ b: " + (a ^ b) + "\n");
    }
}
